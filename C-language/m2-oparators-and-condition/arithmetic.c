#include<stdio.h>

int main(){

    int a = 20;
    int b = 7;

    // Calculate summation
    int sum = a+b;
    printf("Summation = %d\n", sum);
    
    // Calculate subtract
    int sub = a-b;
    printf("Subtract = %d\n", sub);
    
    // Calculate multiplication
    int mul = a*b;
    printf("Multiplication = %d\n", mul);
    
    // Calculate division
    int div = a/b;
    printf("Division = %d\n", div);
    
    // Calculate modulus
    int mod = a%b;
    printf("Modulus = %d\n", mod);


    return 0;
}